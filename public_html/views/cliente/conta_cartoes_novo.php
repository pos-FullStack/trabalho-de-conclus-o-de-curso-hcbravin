<!-- Script para manipulação do carrossel e seleção -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        const carousel = document.getElementById('cardCarousel');
        const cardOptions = document.querySelectorAll('input[name="cardOption"]');
        const requestButton = document.getElementById('requestButton');

        // Sincroniza a seleção do rádio com o slide ativo do carrossel
        // carousel.addEventListener('slid.bs.carousel', function(event) {
        //     const activeIndex = event.to;
        //     cardOptions[activeIndex].checked = true;
        // });

        // Sincroniza o carrossel com a seleção do rádio
        cardOptions.forEach((option, index) => {
            option.addEventListener('change', function() {
                if (this.checked) {
                    const carouselInstance = bootstrap.Carousel.getInstance(carousel);
                    carouselInstance.to(index);
                }
            });
        });

        // Manipula o clique no botão de solicitação
        requestButton.addEventListener('click', function() {
            const selectedCard = document.querySelector('input[name="cardOption"]:checked').value;
            alert(`Solicitação do cartão ${selectedCard.toUpperCase()} enviada com sucesso!\n\nEm breve entraremos em contato para finalizar o processo.`);
        });
    });
</script>